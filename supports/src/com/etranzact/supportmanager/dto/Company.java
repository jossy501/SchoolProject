package com.etranzact.supportmanager.dto;

public class Company {
	
	private String companyId;
	private String companyName;
	private String cardToCredit;
	private String companyCode;
	private String bank;
	private String bankAcct;
	private String bankcode_bankAccount;
	
	public Company()
	{
		
	}

	
	
	public String getCompanyId() {
		return companyId;
	}



	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}



	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getCardToCredit() {
		return cardToCredit;
	}

	public void setCardToCredit(String cardToCredit) {
		this.cardToCredit = cardToCredit;
	}



	public String getCompanyCode() {
		return companyCode;
	}



	public void setCompanyCode(String companyCode) {
		this.companyCode = companyCode;
	}



	public String getBank() {
		return bank;
	}



	public void setBank(String bank) {
		this.bank = bank;
	}



	public String getBankAcct() {
		return bankAcct;
	}



	public void setBankAcct(String bankAcct) {
		this.bankAcct = bankAcct;
	}



	public String getBankcode_bankAccount() {
		return bankcode_bankAccount;
	}


	public void setBankcode_bankAccount(String bankcode_bankAccount) {
		this.bankcode_bankAccount = bankcode_bankAccount;
	}
	
	
	
	
}
