package com.etranzact.cms.dto;

public class FundGateInfo {

    String terminalId ;
    String merchantName ;
    String masterKey ;
    String cardnum ;
    String expiration ;
    String currency ;
    String accountType ;
    String allowCredit ;
    String chargeCardId ;
    String vtuCom ;
    String createDat;
    
    public FundGateInfo()
    {
    	
    }

	public String getTerminalId() {
		return terminalId;
	}

	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}

	public String getMerchantName() {
		return merchantName;
	}

	public void setMerchantName(String merchantName) {
		this.merchantName = merchantName;
	}

	public String getMasterKey() {
		return masterKey;
	}

	public void setMasterKey(String masterKey) {
		this.masterKey = masterKey;
	}

	public String getCardnum() {
		return cardnum;
	}

	public void setCardnum(String cardnum) {
		this.cardnum = cardnum;
	}

	public String getExpiration() {
		return expiration;
	}

	public void setExpiration(String expiration) {
		this.expiration = expiration;
	}

	public String getCurrency() {
		return currency;
	}

	public void setCurrency(String currency) {
		this.currency = currency;
	}

	public String getAccountType() {
		return accountType;
	}

	public void setAccountType(String accountType) {
		this.accountType = accountType;
	}

	public String getAllowCredit() {
		return allowCredit;
	}

	public void setAllowCredit(String allowCredit) {
		this.allowCredit = allowCredit;
	}

	public String getChargeCardId() {
		return chargeCardId;
	}

	public void setChargeCardId(String chargeCardId) {
		this.chargeCardId = chargeCardId;
	}

	public String getVtuCom() {
		return vtuCom;
	}

	public void setVtuCom(String vtuCom) {
		this.vtuCom = vtuCom;
	}

	public String getCreateDat() {
		return createDat;
	}

	public void setCreateDat(String createDat) {
		this.createDat = createDat;
	}
    
    
    
    
    
}
